Drop Table if exists Mushrooms;

Create Table Mushrooms(

	classification varchar(30),
	cap_diameter float,
	cap_shape varchar(30),
	cap_surface varchar(30),
	cap_color varchar(30),
	does_bruise_or_bleed varchar(30),
	gill_attachment varchar(30),
	gill_spacing varchar(30),
	gill_color varchar(30),
	stem_height float,
	stem_width float,
	stem_root varchar(30),
	stem_surface varchar(30),
	stem_color varchar(30),
	veil_type varchar(30),
	veil_color varchar(30),
	has_ring varchar(30),
	ring_type varchar(30),
	spore_print_color varchar(30),
	habitat varchar(30),
	season varchar(30)
	);

Select * From Mushrooms;

